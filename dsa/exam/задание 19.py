def solution(arr):
    """
    Сортировка вставками без бинарного поиска.
    Возвращает количество перестановок.
    """
    count = 0
    for i in range(1, len(arr)):
        key = arr[i]
        j = i - 1

        # Пока есть элементы больше key — сдвигаем их вправо
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            count += 1  # Считаем каждую перестановку
            j -= 1

        arr[j + 1] = key  # Вставка элемента на место
    return count
